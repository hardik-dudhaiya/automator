package com.smileparser.automator.Model;

public class Action {

    private String img_action_name;
    private String img_action_sub_name;
    private String tv_action_label;
    private String tv_action_sub_label;

    public String getImg_action_name() {
        return img_action_name;
    }

    public void setImg_action_name(String img_action_name) {
        this.img_action_name = img_action_name;
    }

    public String getImg_action_sub_name() {
        return img_action_sub_name;
    }

    public void setImg_action_sub_name(String img_action_sub_name) {
        this.img_action_sub_name = img_action_sub_name;
    }

    public String getTv_action_label() {
        return tv_action_label;
    }

    public void setTv_action_label(String tv_action_label) {
        this.tv_action_label = tv_action_label;
    }

    public String getTv_action_sub_label() {
        return tv_action_sub_label;
    }

    public void setTv_action_sub_label(String tv_action_sub_label) {
        this.tv_action_sub_label = tv_action_sub_label;
    }
}
