package com.smileparser.automator.Model;

public class Trigger {

    private String img_trigger_name;
    private String img_trigger_sub_name;
    private String tv_trigger_label;
    private String tv_trigger_sub_label;


    public String getImg_trigger_name() {
        return img_trigger_name;
    }

    public void setImg_trigger_name(String img_trigger_name) {
        this.img_trigger_name = img_trigger_name;
    }

    public String getImg_trigger_sub_name() {
        return img_trigger_sub_name;
    }

    public void setImg_trigger_sub_name(String img_trigger_sub_name) {
        this.img_trigger_sub_name = img_trigger_sub_name;
    }

    public String getTv_trigger_label() {
        return tv_trigger_label;
    }

    public void setTv_trigger_label(String tv_trigger_label) {
        this.tv_trigger_label = tv_trigger_label;
    }

    public String getTv_trigger_sub_label() {
        return tv_trigger_sub_label;
    }

    public void setTv_trigger_sub_label(String tv_trigger_sub_label) {
        this.tv_trigger_sub_label = tv_trigger_sub_label;
    }
}
