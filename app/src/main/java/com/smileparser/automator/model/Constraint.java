package com.smileparser.automator.model;

public class Constraint {
    private String img_constraint_name;
    private String img_constraint_sub_name;
    private String tv_constraint_label;
    private String tv_constraint_sub_label;

    public String getImg_constraint_name() {
        return img_constraint_name;
    }

    public void setImg_constraint_name(String img_constraint_name) {
        this.img_constraint_name = img_constraint_name;
    }

    public String getImg_constraint_sub_name() {
        return img_constraint_sub_name;
    }

    public void setImg_constraint_sub_name(String img_constraint_sub_name) {
        this.img_constraint_sub_name = img_constraint_sub_name;
    }

    public String getTv_constraint_label() {
        return tv_constraint_label;
    }

    public void setTv_constraint_label(String tv_constraint_label) {
        this.tv_constraint_label = tv_constraint_label;
    }

    public String getTv_constraint_sub_label() {
        return tv_constraint_sub_label;
    }

    public void setTv_constraint_sub_label(String tv_constraint_sub_label) {
        this.tv_constraint_sub_label = tv_constraint_sub_label;
    }
}

